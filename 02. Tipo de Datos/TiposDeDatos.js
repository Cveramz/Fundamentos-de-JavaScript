/* TIPOS DE DATOS */

// Numeros:
console.log(44);
console.log(39.5);

// Textos (Strings):
console.log("Hola Mundo");
console.log("Hello World");

// Logicos (Booleanos):
console.log(true);
console.log(false);

// Indefinido (Undefined):
console.log(undefined);

// Nulo (Null):
console.log(null);

/* TIPO DE DATOS ESTRUCTURALES */

console.log({ nombre: "Juan", apellido: "Perez" }); // Objeto

// Listas (Arrays):

console.log(["Manzana", "Pera", "Banana"]); // Lista de Strings
